public class EjercicioWhile2JoseRodriguez {
    private String nombre;

    public EjercicioWhile2JoseRodriguez(String nombre) {
        this.nombre = nombre;
    }
    public int c(int contador, int numMax) {
        int resultado = 0;
        while (contador < numMax) {
            contador += 1;
        }
        return resultado;}
}

